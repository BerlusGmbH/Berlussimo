function connect() {
	$con = mysql_connect ( DB_HOST, DB_USER, DB_PASS ) or die ( mysql_error () );
	mysql_set_charset('utf8',$con);
	mysql_select_db ( DB_NAME, $con ) or die ( mysql_error () );
}

